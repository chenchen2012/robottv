#!/usr/bin/env node
import fs from "node:fs/promises";
import path from "node:path";

const ROOT = process.cwd();
const REPORT_JSON =
  process.env.SEO_REPORT_JSON ||
  path.join(ROOT, "ops-private", "reports", "seo", "weekly-seo-check.json");
const OUT_SUBJECT =
  process.env.SEO_EMAIL_SUBJECT_FILE ||
  path.join(ROOT, "ops-private", "reports", "seo", "weekly-seo-email-subject.txt");
const OUT_BODY =
  process.env.SEO_EMAIL_BODY_FILE ||
  path.join(ROOT, "ops-private", "reports", "seo", "weekly-seo-email-body.txt");

function fmtDate(iso) {
  const d = new Date(iso);
  if (Number.isNaN(d.getTime())) return iso;
  return d.toISOString().slice(0, 10);
}

function statusIcon(status) {
  if (status === "pass") return "PASS";
  if (status === "warn") return "WARN";
  return "FAIL";
}

function listLines(title, arr) {
  if (!Array.isArray(arr) || !arr.length) return "";
  return `${title}\n${arr.map((x) => `- ${x}`).join("\n")}\n\n`;
}

async function run() {
  const raw = await fs.readFile(REPORT_JSON, "utf8");
  const report = JSON.parse(raw);
  const stamp = report.timestamp || new Date().toISOString();
  const day = fmtDate(stamp);
  const state = String(report.status || "unknown").toLowerCase();
  const s = report.summary || {};

  const subject = `[robot.tv SEO] ${statusIcon(state)} weekly check - ${day}`;
  const body = [
    `robot.tv weekly SEO check`,
    ``,
    `Timestamp: ${stamp}`,
    `Status: ${statusIcon(state)}`,
    ``,
    `Summary`,
    `- HTML pages scanned: ${s.htmlPagesScanned ?? "n/a"}`,
    `- Missing required SEO files: ${s.requiredMissing ?? "n/a"}`,
    `- Missing high-value pages: ${s.highValueMissing ?? "n/a"}`,
    `- Missing canonical tags: ${s.canonicalMissing ?? "n/a"}`,
    `- Unexpected noindex pages: ${s.unexpectedNoindex ?? "n/a"}`,
    `- Broken local links: ${s.brokenLocalLinks ?? "n/a"}`,
    ``,
    listLines("Failed checks", report.failedChecks || []),
    listLines("Warnings", report.warnings || []),
    `Generated by weekly-seo-check workflow in the robot.tv repository.`,
    ``
  ]
    .join("\n")
    .replace(/\n{3,}/g, "\n\n");

  await fs.mkdir(path.dirname(OUT_SUBJECT), { recursive: true });
  await fs.writeFile(OUT_SUBJECT, `${subject}\n`, "utf8");
  await fs.writeFile(OUT_BODY, `${body}\n`, "utf8");

  console.log(`subject_file=${OUT_SUBJECT}`);
  console.log(`body_file=${OUT_BODY}`);
  console.log(subject);
}

run().catch((err) => {
  console.error(err);
  process.exit(1);
});
